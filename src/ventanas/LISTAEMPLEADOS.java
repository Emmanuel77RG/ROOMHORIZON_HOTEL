    private void SeleccionarClienteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SeleccionarClienteButtonActionPerformed
        // TODO add your handling code here:
        int filaSeleccionada = clientesTable.getSelectedRow();
       
        if(filaSeleccionada==-1){
            JOptionPane.showMessageDialog(null, "Por favor, seleccione una fila", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        int idCliente = (int) clientesTable.getValueAt(filaSeleccionada, 0);
        String nombre = (String) clientesTable.getValueAt(filaSeleccionada, 1); // Supongamos columna 1 es "nombre"
        String apellido = (String) clientesTable.getValueAt(filaSeleccionada, 2); // Columna 2 es "apellido"
        String correo = (String) clientesTable.getValueAt(filaSeleccionada, 3); // Columna 3 es "correo"
        String numero = (String) clientesTable.getValueAt(filaSeleccionada, 4); // Columna 4 es "número"

        // Creación del objeto Cliente usando los datos obtenidos
        Cliente cliente = new Cliente(nombre, apellido, correo, numero);
        cliente.setId_cliente(idCliente);

        // Asignación a textFieldCliente en la clase CREARRESERVAS
        crearReserva.setClienteSeleccionado(cliente);
        dispose();

    }//GEN-LAST:event_SeleccionarClienteButtonActionPerformed

