    private void rellenarDatosTabla(ResultSet resultados) throws SQLException{
        DefaultTableModel modelo =(DefaultTableModel)clientesTable.getModel();
        modelo.setRowCount(0);
        while(resultados.next()){
            int id_cliente=resultados.getInt("Id_cliente");
            String nombre=resultados.getString("Nombre");
            String apellido=resultados.getString("Apellido");
            String correo=resultados.getString("Correo");
            String numero=resultados.getString("Numero");
            modelo.addRow(new Object[]{id_cliente,nombre,apellido,correo,numero});
        }
        
        
    }
